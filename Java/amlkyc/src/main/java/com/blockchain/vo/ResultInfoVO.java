package com.blockchain.vo;

public class ResultInfoVO {

	private String resultStatus;
	private String resultCodeId;
	private String resultDescription;
	private String resultComments;
	
	public String getResultStatus() {
		return resultStatus;
	}
	public void setResultStatus(String resultStatus) {
		this.resultStatus = resultStatus;
	}
	public String getResultCodeId() {
		return resultCodeId;
	}
	public void setResultCodeId(String resultCodeId) {
		this.resultCodeId = resultCodeId;
	}
	public String getResultDescription() {
		return resultDescription;
	}
	public void setResultDescription(String resultDescription) {
		this.resultDescription = resultDescription;
	}
	public String getResultComments() {
		return resultComments;
	}
	public void setResultComments(String resultComments) {
		this.resultComments = resultComments;
	}	
}
